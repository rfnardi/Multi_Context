    local dict = M.dict[lang] or M.dict["pt-BR"]
    local str = dict[key]
    if not str and lang ~= "pt-BR" then str = M.dict["pt-BR"][key] end
    str = str or key
    if select('#', ...) > 0 then return string.format(str, ...) end
    return str
end

